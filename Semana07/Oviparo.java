public class Oviparo extends Animais {
    private int qtdOlhos;
    private String fecundacao;

    public void setQtdOlhos(int qtdOlhos) {
        this.qtdOlhos = qtdOlhos;
    }

    public int getQtdOlhos() {
        return qtdOlhos;
    }

    public void setFecundacao(String fecundacao) {
        this.fecundacao = fecundacao;
    }

    public String getFecundacao() {
        return fecundacao;
    }
}